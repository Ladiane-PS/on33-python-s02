#Atividade 1: Declare variáveis de diferentes tipos e mostre seus valores e tipos.
inteiro = 10
flutuante = 10.5
booleano = True
string = "olá, mundo!"
lista = [1, 2, 3]
tupla = (4, 5, 6)
dicionario = {"nome": "alive", "idade": 25}

#mostrando os valores e os tipos das variáveis
print(f"inteiro: {inteiro}, tipo: {type(inteiro)}")
print(f"flutuante: {flutuante}, tipo: {type(flutuante)}")
print(f"booleano: {booleano}, tipo: {type(booleano)}")
print(f"string: {string}, tipo: {type(string)}")
print(f"lista: {lista}, tipo: {type(lista)}")
print(f"tupla: {tupla}, tipo: {type(tupla)}")
print(f"dicionario: {dicionario}, tipo: {type(dicionario)}")




