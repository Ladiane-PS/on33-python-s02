#\Adição
soma = 5 + 3
print (soma) ### o resultado deverá ser 8

#\Subtração
subtracao = 5 - 3
print (subtracao) ### o resultado deverá ser 2

# Multiplicação
multiplicacao = 5 * 3
print (multiplicacao) ### o resultado deverá ser 15

# Divisão 
divisao = 5 / 3
print (divisao) ### o resultado deverá ser 1.6666666666666667

# Divisão inteira
divisao_inteira = 5 // 3
print (divisao_inteira) ### o resultado deverá ser 1

# Modulo
modulo = 20 % 6
print (modulo) ### o resultado deverá ser 2 - o resto da divisão

# Exponencial
exponencial = 2 ** 3
print (exponencial) ### o resultado deverá ser 125

